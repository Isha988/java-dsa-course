public class L11TypeCasting {
    public static void main(String[] args) {
        float a = 234.908f;
        int b = (int) a;
        System.out.println(b);

        char A = 'A';
        char B = 'B';
        int num1 = A;
        int num2 = B;
        System.out.println(num1);
        System.out.println(num2);
    }
    
}