public class L4DataTypes {
    public static void main(String[] args) {
        // byte data type
        // 1 byte size (-128 to 127)
        byte byte1 = 99; // less used
        System.out.println(byte1);

        // character data type
        // 2 bytes
        char ch = 't';   // store only character and use single quotes
        System.out.println(ch); 
        
        // boolean data type
        // 1 byte
        boolean bool = true;
        System.out.println(bool);

        // float data type
        // 4 bytes
        float decimal = 56.90F;
        System.out.println(decimal);

        // int data type
        // 4 bytes
        int num = 789;
        System.out.println(num);

        // long data type
        // 8 bytes
        long num2 = 999999999;
        System.out.println(num2);

        // double data type
        // 8 bytes
        double num3 = 999999999.99999999999F;
        System.out.println(num3); 

        // short data type
        // 2 bytes
        short num4 = 8989;  // less used
        System.out.println(num4);

    }    
}
