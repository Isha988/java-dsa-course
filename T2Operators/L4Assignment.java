package T2Operators;

public class L4Assignment {
    public static void main(String[] args) {
        int a = 55;
        System.out.println("A = " + a);
        a += 10;
        System.out.println("A += 10: A = " + a);
        a -= 10;
        System.out.println("A -= 10: A = " + a);
        a *= 10;
        System.out.println("A *= 10: A = " + a);
        a /= 10;
        System.out.println("A /= 10: A = " + a);
    }
}
