package T3ConditionalStatement;

public class L9Switch {
    public static void main(String[] args) {
        int num = 5;

        switch (num) {
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3: 
                System.out.println("Three");
            default:
                System.out.println("Default");
                break;
        }
    }
    
}