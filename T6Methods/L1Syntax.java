// Write a function to print hello world three times

// syntax of method 
/*
 * return_type function_name() {
 *      // body
 *      return statement;
 * } 
*/

package T6Methods;

public class L1Syntax {
    public static void main(String[] args) {
        printHelloWorld();
    }
    
    static void printHelloWorld() {
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
    }
}